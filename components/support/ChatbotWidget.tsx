const normalizeArabicText = (text: string) =>
  text
    .toLowerCase()
    .replace(/[\u064B-\u065F\u0670]/g, '')
    .replace(/[أإآ]/g, 'ا')
    .replace(/ى/g, 'ي')
    .replace(/ؤ/g, 'و')
    .replace(/ئ/g, 'ي')
    .replace(/ة/g, 'ه')
    .replace(/ـ/g, '')
    .replace(/[^\p{L}\p{N}\s]/gu, ' ')
    .replace(/\s+/g, ' ')
    .trim();

const keywordMatchesQuery = (query: string, keyword: string) => {
  const normalizedKeyword = normalizeArabicText(keyword);
  if (!normalizedKeyword) return false;

  if (query.includes(normalizedKeyword)) return true;

  const queryTokens = query.split(' ').filter(Boolean);
  const keywordTokens = normalizedKeyword.split(' ').filter(Boolean);

  if (keywordTokens.length === 1) {
    return queryTokens.includes(normalizedKeyword);
  }

  return keywordTokens.every(token => queryTokens.includes(token));
};

